/**
 * 从词库中随机抽取 n 个不重复词语
 * @param {number} n 抽取数量
 * @returns {string[]}
 */
function getRandomWords(n = 3) {
  const pool = [...WORD_BANK];
  const result = [];
  for (let i = 0; i < n && pool.length > 0; i++) {
    const idx = Math.floor(Math.random() * pool.length);
    result.push(pool.splice(idx, 1)[0]);
  }
  return result;
}

